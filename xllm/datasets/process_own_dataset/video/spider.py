names = ["test.json","train.json"]

for name in names:
    with open(name, 'r', encoding='UTF-8') as f:
        json_data = json.load(f)
        for key in json_data.keys():
            headers = {
            # change the token
                'User-Agent': agent_headers[random.randint(0, len_headers - 1)]
            }
            try:
                print("download {}".format(key))
                path_name = os.path.join(base_dir,key + ".txt")
                if os.path.exists(path_name):
                    print("{} has been recorded".format(key))
                    continue
                response = requests.get(url.format(key),headers = headers).text

                response = json.loads(response)
                if len(response["item_list"]) == 0:
                    print("{} has been deleted".format(key))
                    continue
                download_url = str(response["item_list"][0]["video"]["play_addr"]["url_list"][0]).replace('playwm','play')
                with open(os.path.join(base_dir,key + ".txt"),"w") as f:
                    f.write(download_url)
                time.sleep(1)
            except Exception as e:
                print(e)
                time.sleep(10)